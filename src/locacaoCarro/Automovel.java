package locacaoCarro;

public class Automovel extends Modelo {
	
	private String placa;
	private String cor;
	private int num_portas;
	private String tipo_combustivel;
	private double km;
	private double renavam;
	private double chassi;
	private double valor_locacao;
	
	
	
	
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public int getNum_portas() {
		return num_portas;
	}
	public void setNum_portas(int num_portas) {
		this.num_portas = num_portas;
	}
	public String getTipo_combustivel() {
		return tipo_combustivel;
	}
	public void setTipo_combustivel(String tipo_combustivel) {
		this.tipo_combustivel = tipo_combustivel;
	}
	public double getKm() {
		return km;
	}
	public void setKm(double km) {
		this.km = km;
	}
	public double getRenavam() {
		return renavam;
	}
	public void setRenavam(double renavam) {
		this.renavam = renavam;
	}
	public double getChassi() {
		return chassi;
	}
	public void setChassi(double chassi) {
		this.chassi = chassi;
	}
	public double getValor_locacao() {
		return valor_locacao;
	}
	public void setValor_locacao(double valor_locacao) {
		this.valor_locacao = valor_locacao;
	}
	
	
	
}

